""" 2) Faça um script que leia um número e mostre o seu dobro, seu triplo e a sua raiz quadrada """

num = int(input("Digite um número inteiro positivo: "))

dobro = (num * 2)
triplo = (num * 3)
raiz = (num ** 0.5)

print("O número digitado foi", num, "\nSeu dobro é: ", dobro, "\nSeu triplo é: ", triplo, "\nSua raiz quadrada é: ", raiz)
